<template>
  <div>
    <p class="m-4 is-size-2">Case</p>
    <case :case_="case_" v-if="case_"></case>
  </div>
</template>

<script lang="ts">
import Vue from "vue";

import { CaseSerializer } from "../autogenerated-interfaces";
import Case from "../components/Case.vue";
import http from "../http";
import eventBus from "../event-bus";

export default Vue.extend({
  components: { Case },

  data() {
    return {
      case_: null as CaseSerializer | null,
    };
  },

  mounted() {
    eventBus.$on("update:case", this.updateCase);
  },

  async created() {
    this.case_ = await http.fetchDataOrNull(
      `/api/client-contact/case/${this.$route.params.uuid}/`
    );
  },

  methods: {
    async updateCase() {
      const updated = await http.postFetchDataOrNull(
        `/api/client-contact/update-case/${this.$route.params.uuid}/`
      );
      Object.assign(this.case_, updated);
    },
  },
});
</script>
