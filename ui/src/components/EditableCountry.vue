<template>
  <country-selector
    ref="countrySelector"
    v-if="countryEditable && state === State.Selecting"
    :candidateCountries="candidateCountries"
    @change:country="handleUpdateCountry"
    class="country-selector"
  />
  <div v-else @click="handleClick">
    <country :country="country" :countryEditable="countryEditable" />
  </div>
</template>

<script lang="ts">
import Vue, { PropType } from "vue";
import { BAutocomplete } from "buefy/src/components/autocomplete";

import Country from "./Country.vue";
import CountrySelector from "./CountrySelector.vue";
import { CountrySerializer } from "../autogenerated-interfaces";
import { countryIsNull } from "@/factories";
import { isClientContact } from "@/role";
import eventBus from "@/event-bus";
import http from "../http";

type CountrySelectorType = InstanceType<typeof CountrySelector>;
type BAutocompleteType = InstanceType<typeof BAutocomplete>;

enum State {
  Displaying,
  Selecting,
}

export default Vue.extend({
  props: {
    country: Object as PropType<CountrySerializer>,
    countryEditable: Boolean,
  },

  components: {
    Country,
    CountrySelector,
  },

  data() {
    return {
      state: State.Selecting,
      State,
      candidateCountries: [] as CountrySerializer[],
    };
  },

  async created() {
    if (this.hasCountry) {
      this.state = State.Displaying;
    }
    this.candidateCountries =
      (await http.fetchDataOrNull("/api/countries/")) || [];
  },

  computed: {
    hasCountry(): boolean {
      return !countryIsNull(this.country);
    },

    // TODO: make this a method
    canUpdateCountry(): boolean {
      return isClientContact();
    },
  },

  methods: {
    handleUpdateCountry(country: CountrySerializer) {
      if (!country) {
        // FIXME: why
        console.log("ERROR: country is", JSON.stringify(country));
        return;
      }
      eventBus.$emit("update:country", country);
      this.state = State.Displaying;
    },

    handleClick() {
      if (this.state === State.Displaying) {
        if (this.canUpdateCountry) {
          this.state = State.Selecting;
          this.$nextTick(() => {
            const countrySelector = this.$refs
              .countrySelector as CountrySelectorType;
            const autocomplete: BAutocompleteType =
              countrySelector.$refs.autocomplete;
            const input: HTMLElement = autocomplete.$refs.input.$refs.input;
            input.focus();
          });
        }
      }
    },
  },
});
</script>

