<template>
  <div>
    <div class="card" style="overflow: visible">
      <case-table
        :rows="[case_]"
        :casesEditable="[caseEditable]"
        :disabledColumns="disabledColumns"
        :showProviders="true"
        :focusable="false"
        :paginated="false"
      />
    </div>

    <div
      v-if="case_ && haveProcess"
      class="card mt-6"
      style="overflow: visible"
    >
      <div class="card-content">
        <process
          :case_="case_"
          :caseEditable="caseEditable"
          :showSteps="showSteps"
        />
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import Vue, { PropType } from "vue";

import CaseTable from "./CaseTable.vue";
import Process from "./Process.vue";
import { CaseSerializer } from "../autogenerated-interfaces";
import {
  applicantIsNull,
  countryIsNull,
  processIsNull,
  routeIsNull,
} from "../factories";

export default Vue.extend({
  props: {
    case_: Object as PropType<CaseSerializer>,
    caseEditable: {
      type: Object,
      default: () => ({
        applicant: false,
        country: false,
        dateRange: false,
        provider: false,
        route: false,
        steps: { provider: false },
      }),
    },
    showSteps: { type: Boolean, default: true },
  },

  components: {
    CaseTable,
    Process,
  },

  computed: {
    disabledColumns(): Record<string, boolean> {
      return {
        applicant: false,
        hostCountry: applicantIsNull(this.case_.applicant),
        dates: countryIsNull(this.case_.process.route.host_country),
        route: !(this.case_.target_entry_date && this.case_.target_exit_date),
        provider: routeIsNull(this.case_.process.route),
      };
    },

    haveProcess(): boolean {
      return !processIsNull(this.case_.process);
    },
  },
});
</script>
