<template>
  <div class="field has-addons" style="width: 100%">
    <p class="control" style="width: 100%">
      <b-field :label="label">
        <b-autocomplete
          ref="autocomplete"
          v-model="input"
          field="displayName"
          :data="filteredCandidates"
          @select="
            (providerContact) =>
              $emit('select:provider-contact', providerContact)
          "
          @blur="$emit('blur')"
          :openOnFocus="true"
          :keep-first="true"
          :placeholder="placeholder"
          dropdown-position="bottom"
          max-height="100vh"
        >
          <template slot-scope="props">
            <provider
              v-if="giveAppearanceOfSelectingProvider"
              :provider="props.option.provider"
            />
            <provider-contact v-else :provider_contact="props.option" />
          </template>
        </b-autocomplete>
      </b-field>
    </p>
  </div>
</template>

<script lang="ts">
import Vue, { PropType } from "vue";
import { BAutocomplete as BAutocompleteInstance } from "buefy/src/components/autocomplete";
type BAutocomplete = InstanceType<typeof BAutocompleteInstance>;

import { inputMatchesString } from "@/utils";
import {
  ProcessSerializer,
  ProviderContactSerializer,
} from "../autogenerated-interfaces";
import { dismissMobileKeyboardOnDropdownScroll } from "../componentUtils";
import { processIsNull } from "@/factories";
import http from "../http";
import Provider from "./Provider.vue";
import ProviderContact from "./ProviderContact.vue";

export default Vue.extend({
  components: { Provider, ProviderContact },
  props: {
    label: String,
    process: Object as PropType<ProcessSerializer>,
    initialProviderContacts: {
      type: Array as PropType<ProviderContactSerializer[]>,
      default: () => [],
    },
    current: {
      type: Object as PropType<ProviderContactSerializer | null>,
      default: null,
    },
    giveAppearanceOfSelectingProvider: {
      type: Boolean,
      default: false,
    },
    placeholder: String,
  },

  data() {
    return {
      input: "",
      providerContacts: [] as ProviderContactSerializer[],
      primaryContactsOnly: this.giveAppearanceOfSelectingProvider,
    };
  },

  created() {
    if (this.initialProviderContacts.length > 0) {
      this.providerContacts = this.initialProviderContacts;
    } else if (!processIsNull(this.process)) {
      this.fetchProviderContacts(this.process);
    }
  },

  mounted() {
    if (this.current) {
      const autocomplete = this.$refs.autocomplete as BAutocomplete;
      autocomplete.setSelected(
        Object.assign(this.current, {
          displayName: this.displayName(this.current),
        })
      );
    }
    dismissMobileKeyboardOnDropdownScroll(this, "autocomplete");
  },

  watch: {
    process: function (value: ProcessSerializer): void {
      this.fetchProviderContacts(value);
    },
  },

  computed: {
    filteredCandidates(): ProviderContactSerializer[] {
      return this.providerContacts
        .filter((providerContact) =>
          inputMatchesString(this.input, this.displayName(providerContact))
        )
        .map((providerContact) => {
          return Object.assign(providerContact, {
            displayName: this.displayName(providerContact),
          });
        });
    },
  },

  methods: {
    async fetchProviderContacts(process_: ProcessSerializer) {
      if (!process_.uuid) {
        // TODO: why?
        return;
      }
      const url = this.primaryContactsOnly
        ? `/api/client-contact/list-primary-provider-contacts/?process_uuid=${process_.uuid}`
        : `/api/client-contact/list-provider-contacts/?process_uuid=${process_.uuid}`;
      this.providerContacts = (await http.fetchDataOrNull(url)) || [];
    },

    displayName(providerContact: ProviderContactSerializer): string {
      if (this.giveAppearanceOfSelectingProvider) {
        return `${providerContact.provider.name}`;
      } else {
        return `${providerContact.user.first_name} ${providerContact.user.last_name} (${providerContact.provider.name})`;
      }
    },
  },
});
</script>
