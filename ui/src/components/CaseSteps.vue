<template>
  <b-table ref="table" :data="steps" detailed :show-detail-icon="false">
    <b-table-column label="Step" v-slot="props">
      <span class="has-text-weight-bold">
        {{ props.row.sequence_number }}.
        {{ props.row.process_step.service.name }}
      </span>
    </b-table-column>

    <b-table-column label="Provider" width="420" v-slot="props">
      <editable-case-step-provider
        :caseStep="props.row"
        :editingSpec="editingSpec.provider"
        :candidateProviderContacts="candidateProviderContacts"
        @update:case-step="(caseStep) => updateRow(props.index, caseStep)"
      />
    </b-table-column>

    <b-table-column label="Contact" v-slot="props">
      <provider-contact
        v-if="
          props.row.active_contract &&
          props.row.active_contract.provider_contact
        "
        :provider_contact="props.row.active_contract.provider_contact"
      />
    </b-table-column>

    <b-table-column label="Status" v-slot="props">
      {{ props.row.state.value }}
    </b-table-column>

    <b-table-column label="Actions" v-slot="props">
      <ul>
        <li v-for="action of props.row.actions" :key="action.url">
          <action-button
            class="mb-2"
            :action="action"
            :object="props.row"
            @action:success="(caseStep) => updateRow(props.index, caseStep)"
          >
          </action-button>
        </li>
      </ul>
    </b-table-column>

    <b-table-column v-slot="props">
      <template>
        <a @click="props.toggleDetails(props.row)"> Manage files </a>
      </template>
    </b-table-column>

    <template #detail="props">
      <case-step-file-upload-area :step="props.row">
      </case-step-file-upload-area>
    </template>
  </b-table>
</template>

<script lang="ts">
import Vue, { PropType } from "vue";

import BTable from "buefy/src/components/table";
type BTableInstance = InstanceType<typeof BTable>;

import ActionButton from "./ActionButton.vue";
import CaseStepFileUploadArea from "./CaseStepFileUploadArea.vue";
import EditableCaseStepProvider from "./EditableCaseStepProvider.vue";
import ProviderContact from "../components/ProviderContact.vue";
import { EditingSpec } from "@/editable-component";
import {
  CaseStepSerializer,
  ProcessSerializer,
  ProviderContactSerializer,
} from "../autogenerated-interfaces";
import { processIsNull } from "@/factories";
import { isClientContact } from "../role";
import http from "../http";
import eventBus from "../event-bus";

export default Vue.extend({
  components: {
    ActionButton,
    CaseStepFileUploadArea,
    EditableCaseStepProvider,
    ProviderContact,
  },

  props: {
    steps: Array as PropType<CaseStepSerializer[]>,
    process: Object as PropType<ProcessSerializer>,
    editingSpec: Object as PropType<{ provider: EditingSpec }>,
  },

  data() {
    return {
      candidateProviderContacts: [] as ProviderContactSerializer[],
    };
  },

  async created() {
    const process = this.process;
    if (this.canUpdateProvider && !processIsNull(process)) {
      this.fetchProviderContacts(process);
    }
  },

  computed: {
    canUpdateProvider(): boolean {
      return isClientContact() && this.editingSpec.provider.editable;
    },
  },
  watch: {
    process: function (value: ProcessSerializer): void {
      if (this.canUpdateProvider) {
        this.fetchProviderContacts(value);
      }
    },
  },

  methods: {
    // TODO: duplicated in EditableProvider.vue
    async fetchProviderContacts(process_: ProcessSerializer) {
      if (!process_.uuid) {
        // TODO: why?
        console.error(
          "CaseSteps.fetchProviderContacts: process_.uuid is null:",
          process_
        );
        return;
      }
      const url = `/api/client-contact/list-primary-provider-contacts/?process_uuid=${process_.uuid}`;
      this.candidateProviderContacts = (await http.fetchDataOrNull(url)) || [];
    },

    updateRow(index: number, caseStep: CaseStepSerializer | null) {
      const table = this.$refs.table as BTableInstance;
      // TODO: This is presumably mutating a prop: the table data is `steps`,
      // which is passed in as a prop.
      if (caseStep) {
        // TODO: This should eventually be
        // eventBus.$emit("update:case-step", caseStep)
        table.visibleData.splice(index, 1, caseStep);
      } else {
        table.visibleData.splice(index, 1);
      }
    },
  },
});
</script>

